export function getDGSlideshareImages(lessonId: string): any[] {
  const images = dgSlideshareImages[lessonId];
  if (!images) return [];
  
  return images.map(({ slide, caption }) => ({
    type: "image" as const,
    src: `/images/dg-slideshare/dg-slide-${String(slide).padStart(3, '0')}.jpg`,
    caption,
    alt: `Dangerous Goods training slide ${slide}: ${caption}`,
  }));
}
